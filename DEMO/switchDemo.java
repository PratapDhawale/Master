class switchDemo 
{
	public static void main(String[] args) 
	{
		int b=1000;
	//	byte a=100;
		switch(b)
		{
			case 10:
				System.out.println("case 10");
			break;
			case 100:
				System.out.println("case 100");
			break;
			case 127:
				System.out.println("case 100");
			break;
			
			default:
				System.out.println("default st.");
		}
	}
}
